package tutorial;

public class OperatorAritmatika {
    public static void main(String[] args) {
        int nilai1 = 10;
        int nilai2 = 5;
        
        System.out.println(nilai1 + nilai2);
        System.out.println(nilai1 - nilai2);
        System.out.println(nilai1 * nilai2);
        System.out.println(nilai1 / nilai2);
        System.out.println(nilai1 % nilai2);
    }
    
}
