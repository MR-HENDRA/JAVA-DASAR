package tutorial;

public class OperatorPenugasan {
    public static void main(String[] args) {
        int nilai = 10;
        System.out.println(nilai);
        nilai += 5;
        System.out.println(nilai);
        nilai -= 3;
        System.out.println(nilai);
        nilai *= 2;
        System.out.println(nilai);
        nilai /= 6;
        System.out.println(nilai);
        nilai %= 2;
        System.out.println(nilai);
    }
    
}
