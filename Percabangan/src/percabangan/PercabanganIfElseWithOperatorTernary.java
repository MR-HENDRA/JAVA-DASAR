package percabangan;

public class PercabanganIfElseWithOperatorTernary {
    
    public static void main(String[] args) {
         boolean suka = false;
        String jawaban;

        // menggunakan operator ternary
        jawaban = suka ? "iya" : "tidak";

        // menampilkan jawaban
        System.out.println(jawaban);
        
    }
    
}
